export const getTranslation = (key: keyof typeof landingTranslations, lang: LandingLanguage): string => {
  return landingTranslations[key][lang];
};

// Helper to replace placeholders like {count} or {percent}
export const formatTranslation = (text: string, replacements: Record<string, string | number>): string => {
  let result = text;
  Object.entries(replacements).forEach(([key, value]) => {
    result = result.replace(`{${key}}`, String(value));
  });
  return result;
};
